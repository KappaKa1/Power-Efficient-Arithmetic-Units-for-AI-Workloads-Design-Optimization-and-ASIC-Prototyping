module GEMM_controller #(
  // Individual Parameters depending on the inputs, can potentially calculate the number of OUTPUT bits from INPUT bits.
  parameter int unsigned INPUT_SRAM_NUM_WORDS   	= 64,
  parameter int unsigned INPUT_NUM_BITS  		= 4,
  parameter int unsigned OUTPUT_SRAM_NUM_WORDS  	= 256,
  parameter int unsigned OUTPUT_NUM_BITS	  	= 16, 		// Actually 13, but round to upper 2^x.
  parameter int unsigned NUM_OF_STREAMING_PORTS		= 16,
  
  // Common Parameters
  parameter int unsigned SRAM_DATA_WIDTH        	= 64,
  parameter int unsigned MATRIX_DIMENSION 		= 32*32, 	// What size of matrix is being inputted
  
  // Calculated Parameters, DO NOT TOUCH !!
  parameter int unsigned INPUT_SRAM_ADDR_WIDTH        	= 8,
  parameter int unsigned OUTPUT_SRAM_ADDR_WIDTH        	= 8
)(
  // Global Signals
  input logic 						clk_i,
  input logic 						rst_ni,
  
  // from SRAM Controller to GEMM Controller
  input logic						start_i,
  
  // from GEMM Controller to SRAM
  output logic [INPUT_SRAM_ADDR_WIDTH -1 :0] 	 	gemm_inp_A_addr_o, // A(M,K)
  output logic [INPUT_SRAM_ADDR_WIDTH -1 :0] 	 	gemm_inp_B_addr_o, // B(K,N)
  
  // from GEMM Controller to SRAM Controller
  output logic 						done_o,
  
  // To Shared Register
  output logic 					 	result_valid_o, 
  output logic [OUTPUT_SRAM_ADDR_WIDTH - 1:0]		gemm_out_Y_addr_o 
);

  typedef enum logic [2:0] {IDLE, COMPUTE} gemm_ctrl_state;
  localparam int unsigned OPERAND_COUNT = 32 / 4;
  localparam int unsigned OPERAND_COUNT_WIDTH = $clog2(OPERAND_COUNT);
  
  gemm_ctrl_state state_q, state_d;
  logic [OPERAND_COUNT_WIDTH - 1 : 0] M_count_q, M_count_d;
  logic [OPERAND_COUNT_WIDTH - 1 : 0] K_count_q, K_count_d;
  logic [OPERAND_COUNT_WIDTH - 1 : 0] N_count_q, N_count_d;
  
  logic [INPUT_SRAM_ADDR_WIDTH -1 :0] gemm_inp_A_addr_q, gemm_inp_A_addr_d;
  logic [INPUT_SRAM_ADDR_WIDTH -1 :0] gemm_inp_B_addr_q, gemm_inp_B_addr_d;
  logic done_q1, done_d1, done_q2, done_d2, done_q3, done_d3, done_q4, done_d4;
  logic valid_q1, valid_d1, valid_q2, valid_d2, valid_q3, valid_d3; // Let it delay by 3 more due to computation from GEMM.
  logic [OUTPUT_SRAM_ADDR_WIDTH - 1:0] gemm_out_addr_q1, gemm_out_addr_d1, gemm_out_addr_q2, gemm_out_addr_d2, gemm_out_addr_q3, gemm_out_addr_d3;
  
