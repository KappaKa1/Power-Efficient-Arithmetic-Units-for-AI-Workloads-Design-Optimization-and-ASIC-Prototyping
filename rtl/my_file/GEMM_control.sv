  assign gemm_inp_B_addr_o = gemm_inp_B_addr_q;
  assign done_d2 = done_q1;
  assign done_d3 = done_q2;
  assign done_d4 = done_q3;
  assign done_o = done_q4;
  assign valid_d2 = valid_q1;
  assign valid_d3 = valid_q2;
  assign result_valid_o = valid_q3;
  assign gemm_out_addr_d2 = gemm_out_addr_q1;
  assign gemm_out_addr_d3 = gemm_out_addr_q2;
  assign gemm_out_Y_addr_o = gemm_out_addr_q3;
  
  always_comb begin
    valid_d1 = '0;
    gemm_out_addr_d1 = '0;
    done_d1 = '0;
  
    state_d = state_q;
    M_count_d = M_count_q;
    K_count_d = K_count_q;
    N_count_d = N_count_q;
    gemm_inp_A_addr_d = gemm_inp_A_addr_q;
    gemm_inp_B_addr_d = gemm_inp_B_addr_q;
    
    case (state_q) 
      IDLE: begin
        if (start_i) begin
          M_count_d = '0;
          N_count_d = '0;
          K_count_d = 1'b1;
          gemm_inp_A_addr_d = '0;
          gemm_inp_B_addr_d = '0;
          state_d = COMPUTE;
        end
      end
    
      COMPUTE: begin
        gemm_inp_A_addr_d = M_count_q * OPERAND_COUNT + K_count_q;
        gemm_inp_B_addr_d = N_count_q * OPERAND_COUNT + K_count_q;
        gemm_out_addr_d1 = ((M_count_q * OPERAND_COUNT) + N_count_q) * 4; // We let SRAM controller set the address
        
        if (K_count_q == OPERAND_COUNT-1) begin
          K_count_d = '0;
          valid_d1 = 1'b1;

          if (N_count_q == OPERAND_COUNT-1) begin
            N_count_d = '0;

            if (M_count_q == OPERAND_COUNT-1) begin
              M_count_d = '0;
              done_d1 = 1'b1;
              state_d = IDLE;
            end else begin
              M_count_d = M_count_q + 1;
            end
          end else begin
            N_count_d = N_count_q + 1;
          end
        end else begin
          K_count_d = K_count_q + 1;
        end
      end
      
      default: begin
        state_d = IDLE;
      end
    endcase
  end

  always_ff @(posedge clk_i) begin
    if (!rst_ni) begin
      state_q <= IDLE;
      M_count_q <= '0;
      N_count_q <= '0;
      K_count_q <= '0;
      gemm_inp_A_addr_q <= '0;
      gemm_inp_B_addr_q <= '0;
      done_q1 <= '0;
      done_q2 <= '0;
      done_q3 <= '0;
      done_q4 <= '0;
      valid_q1 <= '0;
      valid_q2 <= '0;
      valid_q3 <= '0;
      gemm_out_addr_q1 <= '0; 
      gemm_out_addr_q2 <= '0;
      gemm_out_addr_q3 <= '0;
    end else begin
      state_q <= state_d;
      M_count_q <= M_count_d;
      N_count_q <= N_count_d;
      K_count_q <= K_count_d;
      gemm_inp_A_addr_q <= gemm_inp_A_addr_d;
      gemm_inp_B_addr_q <= gemm_inp_B_addr_d;
      done_q1 <= done_d1;
      done_q2 <= done_d2;
      done_q3 <= done_d3;
      done_q4 <= done_d4;
      valid_q1 <= valid_d1;
      valid_q2 <= valid_d2;
      valid_q3 <= valid_d3;
      gemm_out_addr_q1 <= gemm_out_addr_d1;
      gemm_out_addr_q2 <= gemm_out_addr_d2;
      gemm_out_addr_q3 <= gemm_out_addr_d3;
    end
  end

